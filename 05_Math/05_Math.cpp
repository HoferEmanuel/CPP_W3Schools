#include <iostream>

//include math header file for sqrt, round, log ...
#include <cmath>

using namespace std;

int main()
{
	cout << "the highest value is " << max(5, 10) << endl;
	cout << "the lowest value is " << min(5, 10) << "\n\n";

	cout << "cmath:" << endl;
	cout << "the squareroot of 64 is " << sqrt(64) << endl;
	cout << "2.6 rounded is " << sqrt(2.6) << endl;
}